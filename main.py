async def handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text.lower()

    # =========================
    # BIENVENIDA AUTOMÁTICA
    # =========================
    if text in ["hola", "buenos dias", "buenas tardes", "buenas noches", "hey", "hello"]:
        context.user_data.clear()

        await update.message.reply_text(
            "👋 Bienvenido a TECHHOME\n\n"
            "🧠 Sistema de gestión de solicitudes técnicas\n"
            "📊 Registro y seguimiento de tickets\n\n"
            "👉 Selecciona una opción del menú:",
            reply_markup=menu
        )
        return

    # =========================
    # INICIO CONSULTA TICKET
    # =========================
    if text == "🔎 consultar ticket":
        context.user_data.clear()
        context.user_data["consultando_ticket"] = True

        await update.message.reply_text(
            "🔎 Escribe tu número de ticket:\n\nEjemplo: TECH-1234"
        )
        return

    # =========================
    # BUSQUEDA DE TICKET
    # =========================
    if context.user_data.get("consultando_ticket"):

        ticket_id = text.strip().upper()

        registros = sheet.get_all_records()

        for row in registros:
            if str(row["TICKET ID"]).upper() == ticket_id:

                estado = row["ESTADO"]

                estado_msg = {
                    "Abierto": "🟡 Recibido y en cola",
                    "En proceso": "🔧 Técnico asignado",
                    "En visita": "🚗 En camino / atendiendo",
                    "Resuelto": "✅ Reparación finalizada",
                    "Cerrado": "🏁 Caso finalizado"
                }.get(estado, estado)

                await update.message.reply_text(
                    f"""🔎 INFORMACIÓN DEL TICKET

📌 ID: {row['TICKET ID']}
📅 Fecha: {row['FECHA']}
👤 Cliente: {row['NOMBRE']}
📞 Tel: {row['TELEFONO']}

Tipo: {row['TIPO']}
Subtipo: {row['SUBTIPO']}

Estado: {estado_msg}

📝 Descripción:
{row['DESCRIPCION']}

🤖 TECHHOME te mantendrá informado."""
                )

                context.user_data["consultando_ticket"] = False
                return

        await update.message.reply_text(
            "❌ No encontramos ese ticket.\nVerifica el ID e intenta nuevamente."
        )

        context.user_data["consultando_ticket"] = False
        return

    # =========================
    # SERVICIO TECNICO
    # =========================
    if text == "🛠 servicio técnico":
        context.user_data.clear()
        context.user_data["flow"] = "servicio"

        await update.message.reply_text(
            "🔧 ¿Qué tipo de equipo es?",
            reply_markup=menu_servicio
        )
        return

    if text in ["gasodoméstico", "electrodoméstico", "cámaras", "otro"]:
        context.user_data["subtipo"] = text
        await update.message.reply_text("📝 Describe el problema:")
        return

    # =========================
    # COTIZACION
    # =========================
    if text == "💰 cotización":
        context.user_data.clear()
        context.user_data["flow"] = "cotizacion"

        await update.message.reply_text(
            "📝 Describe el daño o mantenimiento:"
        )
        return

    # =========================
    # PQRS
    # =========================
    if text == "📩 pqrs":
        context.user_data.clear()
        context.user_data["flow"] = "pqrs"

        await update.message.reply_text(
            "📌 ¿Es Garantía o Reclamo?",
            reply_markup=menu_pqrs
        )
        return

    if text in ["garantía", "reclamo"]:
        context.user_data["subtipo"] = text
        await update.message.reply_text("📝 Describe tu solicitud:")
        return

    # =========================
    # FLUJO GENERAL (CREACIÓN TICKET)
    # =========================
    if "descripcion" not in context.user_data:
        context.user_data["descripcion"] = text
        await update.message.reply_text("👤 Nombre del cliente:")
        return

    if "nombre" not in context.user_data:
        context.user_data["nombre"] = text
        await update.message.reply_text("📞 Teléfono:")
        return

    if "telefono" not in context.user_data:
        context.user_data["telefono"] = text

        ticket_id = generar_id()
        fecha = datetime.now().strftime("%Y-%m-%d %H:%M")

        fila = [
            ticket_id,
            fecha,
            context.user_data.get("nombre"),
            context.user_data.get("telefono"),
            context.user_data.get("flow"),
            context.user_data.get("subtipo"),
            context.user_data.get("descripcion"),
            "Abierto"
        ]

        guardar_ticket(fila)

        correo = f"""
📌 NUEVO TICKET TECHHOME

ID: {ticket_id}
Fecha: {fecha}

Cliente: {context.user_data.get('nombre')}
Tel: {context.user_data.get('telefono')}

Tipo: {context.user_data.get('flow')}
Subtipo: {context.user_data.get('subtipo')}

Descripción:
{context.user_data.get('descripcion')}

Estado: Abierto

Un asesor se comunicará pronto.
"""

        enviar_correo(f"Ticket TECHHOME {ticket_id}", correo)

        await update.message.reply_text(
            f"✅ Ticket creado: {ticket_id}\n📞 Te contactaremos pronto.",
            reply_markup=menu
        )

        context.user_data.clear()
        return

